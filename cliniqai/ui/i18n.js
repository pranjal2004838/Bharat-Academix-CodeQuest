function changeLanguage(lang) {
  localStorage.setItem('selectedLang', lang);
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
        element.placeholder = translations[lang][key];
      } else {
        // preserve child elements like icons if present, though innerText overrides.
        // It's safer to use textContent if there are no child elements.
        // For buttons that might have arrows, we can check if they have specific structure.
        if(key === 'enterClinic' || key === 'viewRecords') {
           element.innerText = translations[lang][key];
        } else {
           element.innerText = translations[lang][key];
        }
      }
    }
  });
}

function initLanguage() {
  const savedLang = localStorage.getItem('selectedLang') || 'en';
  const select = document.getElementById('languageSelect');
  if (select) {
    select.value = savedLang;
  }
  changeLanguage(savedLang);
}

document.addEventListener('DOMContentLoaded', initLanguage);
